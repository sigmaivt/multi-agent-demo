"""
Агент-Критик.

Получает тему и готовую статью, оценивает её по трём критериям:
точность, структура, читаемость. Возвращает текстовую оценку и числовой балл.
"""
import re
from claude_client import call_agent

SYSTEM_PROMPT = """Ты строгий редактор и литературный критик. Оцени предоставленную статью по трём критериям: точность фактов, логика структуры, читаемость текста.

Строго следуй формату ответа:

ОЦЕНКА: [целое число от 1 до 10]

СИЛЬНЫЕ СТОРОНЫ:
- [пункт 1]
- [пункт 2]

ЗАМЕЧАНИЯ:
- [конкретное замечание 1]
- [конкретное замечание 2]
- [конкретное замечание 3]

Отвечай ТОЛЬКО на русском языке. Не отклоняйся от этого формата. Будь конкретен — не пиши общих слов."""


def run(topic: str, article: str) -> tuple:
    """
    Оценивает статью и возвращает результат.

    Args:
        topic: исходная тема статьи
        article: текст статьи от Агента-Писателя

    Returns:
        Tuple (текст оценки: str, числовой балл: int).
        Балл по умолчанию 7 если извлечь число не удалось.
    """
    user_message = f"Тема статьи: {topic}\n\nСтатья:\n{article}"
    feedback = call_agent(SYSTEM_PROMPT, user_message)

    # Извлекаем числовой балл из ответа
    score = 7  # значение по умолчанию
    match = re.search(r"ОЦЕНКА:\s*(\d+)", feedback)
    if match:
        score = max(1, min(10, int(match.group(1))))  # ограничиваем диапазоном 1–10

    return feedback, score
