"""
Агент-Писатель.

Получает тему и тезисы от Исследователя, пишет связную статью.
Опционально принимает замечания от Критика для доработки.
"""
from claude_client import call_agent

SYSTEM_PROMPT = """Ты опытный автор научно-популярных текстов. На основе предоставленных тезисов напиши связную, интересную статью.

Правила:
- Статья должна содержать 4-5 абзацев
- Используй все ключевые тезисы из исследования
- Пиши живым, понятным языком без академического занудства
- Не нумеруй абзацы и не используй заголовки внутри текста
- Выводи ТОЛЬКО текст статьи — без предисловий и послесловий
- Отвечай ТОЛЬКО на русском языке, независимо от языка темы"""


def run(topic: str, research: str, feedback: str = "") -> str:
    """
    Пишет статью на основе исследования.

    Args:
        topic: тема статьи
        research: тезисы от Агента-Исследователя
        feedback: замечания от Агента-Критика (для повторной итерации)

    Returns:
        Текст статьи.
    """
    user_message = f"Тема: {topic}\n\nТезисы от исследователя:\n{research}"

    if feedback:
        user_message += (
            f"\n\nЗамечания критика (обязательно учти их при написании):\n{feedback}"
        )

    return call_agent(SYSTEM_PROMPT, user_message)
