"""
Агент-Синтезатор.

Получает три блока исследований от параллельных агентов (факты, примеры, тренды)
и объединяет их в единый структурированный список тезисов для Писателя.

Это ключевой агент паттерна "параллельные исследователи":
без него Писатель получил бы три несвязанных блока вместо одного цельного.
"""
from claude_client import call_agent

SYSTEM_PROMPT = """Ты аналитик-редактор. Тебе дают три отдельных блока исследования по одной теме:
1. Факты и определения
2. Реальные примеры и кейсы
3. Тренды и прогнозы

Твоя задача — объединить их в ЕДИНЫЙ структурированный список тезисов.

Правила:
- Убери дублирующиеся пункты
- Сохрани самые важные и конкретные тезисы из каждого блока
- Итоговый список: 8-12 пунктов в формате markdown (каждый начинается с "- ")
- Не пиши вступления и заключения — только список
- Отвечай ТОЛЬКО на русском языке"""


def run(topic: str, facts: str, examples: str, trends: str) -> str:
    """
    Синтезирует три блока исследований в единый список тезисов.

    Args:
        topic: исходная тема
        facts: тезисы от Исследователя-Фактов
        examples: тезисы от Исследователя-Примеров
        trends: тезисы от Исследователя-Трендов

    Returns:
        Единый markdown-список тезисов.
    """
    user_message = (
        f"Тема: {topic}\n\n"
        f"=== БЛОК 1: Факты и определения ===\n{facts}\n\n"
        f"=== БЛОК 2: Реальные примеры и кейсы ===\n{examples}\n\n"
        f"=== БЛОК 3: Тренды и прогнозы ===\n{trends}"
    )
    return call_agent(SYSTEM_PROMPT, user_message)
