"""
Агент-Исследователь.

Получает тему и возвращает структурированный список ключевых тезисов.
Его системный промпт заставляет модель выступать в роли аналитика.
"""
from claude_client import call_agent

SYSTEM_PROMPT = """Ты исследователь-аналитик. Твоя задача — изучить заданную тему и собрать ключевые факты, концепции и аргументы.

Правила:
- Выводи ТОЛЬКО структурированный список тезисов в формате markdown (каждый пункт начинается с "- ")
- Минимум 6, максимум 10 тезисов
- Каждый тезис — конкретный, информативный, 1-2 предложения
- Не пиши вступления, заголовки и заключения — только список тезисов
- Отвечай ТОЛЬКО на русском языке, независимо от языка темы"""


def run(topic: str) -> str:
    """
    Исследует тему и возвращает список тезисов.

    Args:
        topic: тема для исследования

    Returns:
        Markdown-список ключевых тезисов.
    """
    user_message = f"Тема для исследования: {topic}"
    return call_agent(SYSTEM_PROMPT, user_message)
